
#include<iostream>
using namespace std;
int main()
{
int arr[5]={1,2,3,2,1};

int size=sizeof(arr)/4;
bool ispalindrome=1;

for(int i=0;i< size/2;i++)
{
if(arr[i]!=arr[size-i-1]){
ispalindrome=0;
}
}
//for loops ends
if (ispalindrome){
	cout<<"Palindrome\n";
}
else{
cout<<"Not palindrome\n";
}
return 0;
}
